package net.hb.project;

import java.util.Date;

public class Customer {
	private int cid;
	private int cpw;
	private String cname;
	
	
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public int getCpw() {
		return cpw;
	}
	public void setCpw(int cpw) {
		this.cpw = cpw;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}			

}
